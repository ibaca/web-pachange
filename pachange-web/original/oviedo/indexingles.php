<p><strong><u>:: What is Pachange? ::</u></strong></p>

<p>Pachange is a project which organises exchange meetings to practice languages. It started Málaga(Andalucía, Spain) in 2006 where it had and go on having succes among the Spanish and Foreign students. Then the same project started in others places of Europe and now in Oviedo!</p>

    <p>&nbsp;</p>

	<p><img name="illustratie" src="img/pachangeoviedo.jpg" alt="group" width="285" border="0" class="right" /></p>

    <p>&nbsp;</p>

    <p><strong><u>:: How does it work? ::</u></strong></p>

    <p> We usually meet every 2-3 weeks in the center of Oviedo (usually in front of the Cathedral), where we assign each Spanish student with one Foreign partner. Later we go to a bar and we spend about an hour practicing Spanish and another one a Foreign language. Every participant pays voluntarily 1Euro for every meeting as a symbolic amount for the work that we are doing.</p>
