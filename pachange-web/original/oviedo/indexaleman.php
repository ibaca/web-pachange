 <p><strong><u>:: Was ist Pachange? ::</u></strong></p>

<p>Pachange: Das heißt Sprachen lernen, nebenbei neue Leute kennen lernen und Fun pur.</p>

    <p>&nbsp;</p>

	<p><img name="illustratie" src="img/pachangeoviedo.jpg" alt="group" width="285" border="0" class="right" /></p>

    <p>&nbsp;</p>

    <p><strong><u>:: Wie läuft Pachange ab? ::</u></strong></p>

    <p> Wir treffen uns alle 2 –3 Wochen im Zentrum von Oviedo (meistens vor der Kathedrale). Dort werden wir zunächst einmal die Pachange Teilnehmer nach ihren Sprachwünschen in 2er bzw. 3 er Grüppchen einteilen. Dann gehen wir alle zusammen in eine ruhige Bar, wo jeder Sprachteilnehmer circa 1 Stunde in seiner Muttersprache bzw. 1 Stunde in der zu lernenden Sprache sprechen wird. Dabei ist der Sprachenvielfalt keine Grenze gesetzt- von Deutsch, Italiensich, Spanisch, Englisch usw...könnt ihr alles lernen und lehren was ihr wollt- vorausgesetzt es gibt den passenden Sprachpartner dazu! Jeder Teilnehmer steuert 1 Euro pro Treffen bei, als symbolische Summe f&uuml;r die Arbeit, die wir machen.</p>



