    <p><strong><u>Bienvenido a Pachange Berlin</u></strong></p>
    <p>&nbsp;</p>
    <p><strong><u>:: &iquest;Qu&eacute; es Pachange? ::</u></strong></p>
    <p>Pachange es un proyecto que tiene como objetivo organizar encuentros de intercambio para practicar idiomas. Se creó en Málaga (Andalucía, España) en 2006 donde tuvo y sigue teniendo mucho éxito entre los estudiantes españoles y extranjeros. Si te gustar&iacute;a practicar un idioma extranjero de forma entretenido y interesante, conocer culturas nuevas y hacer nuevas amistades, entonces Pachange es perfecto para t&iacute;! Ayudas a otros hablando tu idioma nativo o ingl&eacute;s (si tienes un nivel alto) y a cambio te ayudan a t&iacute; con el idioma que quieres mejorar.</p>
    <p></p></td></tr></table>
    <table id="normaltable"><tr>
    <td><p><strong><u>:: ¿Cómo funciona? ::</u></strong></p>
    <p>Quedamos todos en un sitio p&uacute;blico donde asignaremos parejas de intercambio (un extranjero con un alem&aacute;n).

  Luego iremos todos juntos a un lugar tranquilo donde se hablar&aacute; una hora en alem&aacute;n y otra hora

  en espa&ntilde;ol, ingl&eacute;s, franc&eacute;s, etc.. Como a veces se apunta gente y luego no le es possible asistir no siempre podemos asegurar que s&oacute;lo hayan parejas de 2, a veces asignamos grupos de 3 personas. Normalmente cada participante da voluntariamente 1 Euro por encuentro como cantidad simb&oacute;lica por el trabajo que estamos realizando</p> </td><td>
