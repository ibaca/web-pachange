<p><strong><u>:: What is Pachange? ::</u></strong></p>

<p>Pachange is a project which organises exchange meetings to practice languages. It started Málaga(Andalucía, Spain) in 2006 where it had and go on having succes among the Spanish and Foreign students. It is for those persons who are interested in practising any foreign language with a native speaker in exchange for their help with  their own native language. It is an interesting and enjoyable way to meet new cultures and make new friends.</p>
<p>&nbsp;</p>

</td></tr></table>
<table id="normaltable"><tr>
<td><p><strong><u>:: How does it work? ::</u></strong></p>

    <p>After meeting at a public square, where we will assign the exchange partner (one spanish student with a foreign one), we will go together to a quiet place where we will speak one hour in German (if possible) and one hour in English, Spanish, French, Italian and so on. It is possible that we arrange groups of three people sometimes. Normally every participant gives voluntarily 1Euro for every meeting as a symbolic quantity for the work that we are realising.</p></td>
