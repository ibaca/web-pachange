 <p><strong><u>:: Was ist Pachange? ::</u></strong></p>

<p>Pachange ist ein Projekt zur Organisation von Sprachaustauschtreffen. Es fing 2006 in Malaga (Andalusien / Spanien) an und hat seitdem gro&szlig;en Erfolg unter den Spaniern und ausl&auml;ndischen Sprachsch&uuml;lern. Wenn du gerne eine ausl&auml;ndische Sprache auf eine interessante und unterhaltsame Art und Weise   &uuml;ben und nebenbei noch neue Kulturen kennenlernen und neue Freundschaften   schliessen m&ouml;chtest, dann ist Pachange perfekt f&uuml;r dich! Du hilfst anderen eine Sprache zu verbessern, im Austausch f&uuml;r ihre Muttersprache oder ihr Englisch (wenn sie ein hohes Niveau haben).</p>

    <p>&nbsp;</p>
</td></tr></table>
<table id="normaltable"><tr><td><p><strong><u>:: Wie läuft Pachange ab? ::</u></strong></p>

    <p>Wir treffen uns an einem &ouml;ffentlichen Platz, wo wir die Sprachaustauschpartner   einteilen (ein Deutscher mit einem Ausl&auml;nder). Dann gehen wir alle zusammen in   eine ruhige Bar/Cafeter&iacute;a, wo wir dann eine Stunde auf Deutsch und eine Stunde   auf der ausl&auml;ndischen Sprache (Spanisch, Englisch, Franz&ouml;sisch, Italienisch,   etc.) sprechen. Da sich manchmal Leute einschreiben und dann pl&ouml;tzlich aus   irgendeinem Grund nicht kommen k&ouml;nnen, m&uuml;ssen wir dann Gruppen von 3 Personen   einteilen, anstelle von nur zwei Sprachpartnern. Normalerweise steuert jeder Teilnehmer 1 Euro pro Treffen bei, als symbolische Summe f&uuml;r die Arbeit, die wir machen.</p>
