  <div id="maincol" class="clearfix" >
    <p>Das Projekt Pachange hat es sich zum Ziel gesetzt, Sprachaustauschtreffen zu organisieren. Pachange wurde im Juni 2006 in Málaga ins Leben gerufen, wo es noch immer grossen Erfolg unter den spanischen und ausländischen Studenten hat. Seit Fr&uuml;hjahr letzten Jahren finden die Austauschtreffen auch in Gijon statt.</p>
    <p>&nbsp;</p>
	<p><img name="illustratie" src="img/grupo_portada.jpg" alt="group" width="285" height="208" border="0" class="right" /></p>
    <p>&nbsp;</p>
    <p><strong><u>:: Wie läuft das ab? ::</u></strong></p> 
    <p>Wir treffen uns normalerweise alle 2 Wochen , wo wir die Austauschpartner einteilen (ein spanischer und ein ausländischer Student). Danach gehen wir in ein ruhiges Lokal, wo wir eine Stunde auf Spanisch und eine Stunde auf der ausländischen Sprache sprechen.</p>
    
    <img src="http://www.pachange.net/gijon/img/Logo&#32;nuevo&#32;bueno.jpg" alt="logo pachange" width="170" height="140" class="right" /><br />
