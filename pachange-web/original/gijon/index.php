  <div id="maincol" class="clearfix" >
    <p>Pachange es un proyecto que tiene como objetivo organizar encuentros de intercambio para practicar idiomas. Se creó hace un año en Málaga (España) donde tuvo y sigue teniendo mucho éxito entre los estudiantes españoles y extranjeros. En Octube comenzaron a realizarse encuentros de intercambio en Oviedo y esperamos que a partir de Noviembre se puedan realizar también encuentros en Gijón. Para eso necesitamos gente COMO TÚ, dispuesta a aprender y a enseñar.</p>
    <p>&nbsp;</p>

	<p><img name="illustratie" src="img/grupo_portada.jpg" alt="group" width="285" height="208" border="0" class="right" /></p>
    <p>&nbsp;</p>
    <p><strong><u>:: ¿Cómo funciona? ::</u></strong></p> 
    <p> Realizamos encuentros cada dos semanas en un lugar céntrico de la ciudad donde se asignarán las parejas de intercambio (un estudiante extranjero con un estudiante espa&ntilde;ol). Luego vamos a un lugar tranquilo donde se habla una hora en espa&ntilde;ol y una hora  en un idioma extranjero.</p>
    <p>&nbsp;</p>
